    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        if(usernameJTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "User name can't be empty!");
            return;
        }
        if (passwordJPasswordField.getPassword().equals("")) {
            JOptionPane.showMessageDialog(null, "Password can't be empty!");
            return;
        }
        if(nameJTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Name can't be empty!");
            return;
        }
        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();

        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();

        //Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
        Person person = enterprise.getPersonDirectory().createPerson(name);

            //for(UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()){
                
        UserAccount userAccount = enterprise.getUserAccountDirectory().authenticateUser(username, password);
        if (userAccount == null) {
            Enterprise en = (Enterprise) enterpriseJComboBox.getSelectedItem();
            if (en instanceof FitnessClubEnterprise) {
                enterprise.getUserAccountDirectory().createUserAccount(username, password, person, new FitnessClubManagerRole());
            }else {
                enterprise.getUserAccountDirectory().createUserAccount(username, password, person, new OnlineSalesRole());
            }
            JOptionPane.showMessageDialog(null, "Create Successfully!");
        }
        else {
            JOptionPane.showMessageDialog(null, "Account already exist!");
            usernameJTextField.setText("");
            return;
        }
            
        
        populateTable();

    }//GEN-LAST:event_submitJButtonActionPerformed

